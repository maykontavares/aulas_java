package projeto_git;

public class AulaSwitch {

	public static void main(String[] args) {
		int mes = 1
				;
		
		switch (mes) {
		case 1:
			System.out.println("janeiro");			
			break;
		case 2:
			System.out.println("fevereiro");			
			break;
		case 3:
			System.out.println("marco");			
			break;

		default:
			System.out.println("mes nao disponivel");
			break;
		}
	}
	
	
}
